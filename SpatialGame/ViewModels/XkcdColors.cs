    // Lookup function (normalizes name)
    public static bool TryGet(string name, out Color4 color)
    {
        if (string.IsNullOrWhiteSpace(name))
        {
            color = default;
            return false;
        }
        // Normalize: lower, remove spaces, dashes, underscores
        var key = name.ToLowerInvariant().Replace(" ", "").Replace("-", "").Replace("_", "");
        foreach (var kvp in ByName)
        {
            var norm = kvp.Key.ToLowerInvariant().Replace(" ", "").Replace("-", "").Replace("_", "");
            if (norm == key)
            {
                color = kvp.Value;
                return true;
            }
        }
        color = default;
        return false;
    }
